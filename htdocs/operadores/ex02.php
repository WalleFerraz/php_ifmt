<?php
/*
    Exemplos de acumuladores em PHP (soma, substração, divisão e multiplicação)

*/
$valor=0;
$valor+=200; //acumulando 200 na variável $valor
echo $valor;

$valor-=10; //reduzindo 10 da variável $valor
echo '<br>'. $valor;

$compra=345;
$compra*=.9; //apresenta 90% do valor da variável $compra
echo '<br>'. $compra;
?>